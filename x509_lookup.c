static const int csca_len_103 = sizeof(csca_raw_103);
static const int csca_len_101 = sizeof(csca_raw_101);

static const unsigned char *csca_raws[2] = {csca_raw_101, csca_raw_103};
static const int *csca_lens[2] = {&csca_len_101, &csca_len_103};

static X509_STORE *X509_default_lookup(unsigned long issuer_name_hash)
{
    static X509_STORE *store = NULL;

    if (!store)
       store = X509_STORE_new();
    if(!store) {
        printf("Failed to create trust store\n");
        goto err;
    }

    X509 *cert;
    const unsigned char *p;

    assert(sizeof(csca_raws) == sizeof(csca_lens));
    const unsigned int num_entries = sizeof(csca_raws)/sizeof(csca_raws[0]);
    for (unsigned int i=0; i<num_entries; i++) {
        const unsigned char *raw = csca_raws[i];
        const int *len = csca_lens[i];
        if(!raw || !len) {
            printf("raw or len was NULL\n");
            assert(1 != 1);
        }

        /* Set up buf and len to point to the input buffer. */
        p = raw;
        cert = NULL;

        if (d2i_X509(&cert, &p, *len) == NULL);
            /* error */

        if(!X509_STORE_add_cert(store, cert)) {
    //    if (!X509_STORE_load_locations(store, NULL, x509_default_dir)) {
                printf("Failed to load trusted certificates\n");
                X509_STORE_free(store);
                store = NULL;
        }
    }
err:
    return store;
}

X509_lookup_csca_cert EAC_get_default_csca_lookup(void)
{
    return X509_default_lookup;
}

int EAC_CTX_set_csca_lookup(EPASS_CTX *ctx, X509_lookup_csca_cert lookup_csca_cert)
{
    int ok = 0;

    if(!ctx) {
        printf("Invalid EAC context\n");
        goto err;
    }
    ctx->lookup_csca_cert = lookup_csca_cert;
    ok = 1;

err:
    return ok;
}

int EAC_CTX_get_csca_lookup(const EPASS_CTX *ctx, X509_lookup_csca_cert *lookup_csca_cert)
{
    int ok = 0;

    if(!lookup_csca_cert || !ctx) {
        printf("Invalid parameters\n");
        goto err;
    }
    *lookup_csca_cert = ctx->lookup_csca_cert;
    ok = 1;

err:
    return ok;
}
